public class GarageDoor {
    public void up(){ System.out.println("Garage door open");}
    public void down(){ System.out.println("Garage door close");}
    public void stop(){ System.out.println("Stop garage door");}
    public void lightOn(){ System.out.println("Garage light on");}
    public void lightOff(){ System.out.println("Garage light off");}
}
